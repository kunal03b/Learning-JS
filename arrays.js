// Primitive vs reference data types
// Privitive types
let str1 = "Kunal"
let str2 = str1
console.log(str1)
console.log(str2)

str1 = "Sharma"
console.log(str1)
console.log(str2)

// Reference Data types

let arr1 = ["item1","item2"]
let arr2 = arr1
console.log(arr1)
console.log(arr2)

arr1.push("item3")
console.log(arr1)
console.log(arr2)